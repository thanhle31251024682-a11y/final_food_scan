def predict_single(img_path, top_k=3):
    from tensorflow.keras.preprocessing import image as kimage

    img = kimage.load_img(img_path, target_size=(IMG_SIZE, IMG_SIZE))
    x   = kimage.img_to_array(img) / 255.0
    x   = np.expand_dims(x, 0)

    preds  = model.predict(x, verbose=0)[0]
    top    = np.argsort(preds)[::-1][:top_k]
    idx2c  = {v: k for k, v in train_gen.class_indices.items()}

    # Bảng giá theo đề bài
    PRICE = {
        "1. Cơm trắng"    : 10000,
        "10. Trứng chiên"  : 25000,
        "2. Đậu hũ sốt cà": 25000,
        "3. Cá hú kho"    : 30000,
        "4. Thịt kho trứng": 30000,
        "5. Thịt kho"     : 25000,
        "6. Canh chua"    : 25000,
        "7. Sườn nướng"   : 30000,
        "8. Canh rau"     :  7000,
        "9. Rau xào"      : 10000,
    }

    plt.figure(figsize=(4, 4))
    plt.imshow(img); plt.axis("off")
    lines = [f"#{i+1}  {idx2c[t]}: {preds[t]*100:.1f}%" for i, t in enumerate(top)]
    plt.title("\n".join(lines), fontsize=8, loc="left")
    plt.tight_layout(); plt.show()

    best_cls = idx2c[top[0]]
    print(f"Kết quả: {best_cls}")
    print(f"Giá:     {PRICE.get(best_cls, 0):,} đ")
    return best_cls, PRICE.get(best_cls, 0)

