# ── Plot training history ──────────────────────────────────
def plot_history(h1, h2):
    a1  = h1.history["accuracy"];       va1 = h1.history["val_accuracy"]
    l1  = h1.history["loss"];           vl1 = h1.history["val_loss"]
    a2  = h2.history["accuracy"];       va2 = h2.history["val_accuracy"]
    l2  = h2.history["loss"];           vl2 = h2.history["val_loss"]

    acc   = a1 + a2;  vacc  = va1 + va2
    loss  = l1 + l2;  vloss = vl1 + vl2
    ep    = range(1, len(acc) + 1)
    p1end = len(a1)

    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(14, 5))
    fig.suptitle("Training History — Canteen Model", fontsize=13, fontweight="bold")

    ax1.plot(ep, acc,  color="#2196F3", label="Train")
    ax1.plot(ep, vacc, color="#FF5722", ls="--", label="Val")
    ax1.axvline(p1end, color="gray", ls=":", lw=1.5, label=f"Phase 1/2 (ep {p1end})")
    ax1.set(title="Accuracy", xlabel="Epoch", ylabel="Accuracy")
    ax1.yaxis.set_major_formatter(mticker.PercentFormatter(xmax=1))
    ax1.legend(); ax1.grid(alpha=0.3)

    ax2.plot(ep, loss,  color="#2196F3", label="Train")
    ax2.plot(ep, vloss, color="#FF5722", ls="--", label="Val")
    ax2.axvline(p1end, color="gray", ls=":", lw=1.5, label=f"Phase 1/2 (ep {p1end})")
    ax2.set(title="Loss", xlabel="Epoch", ylabel="Loss")
    ax2.legend(); ax2.grid(alpha=0.3)

    plt.tight_layout()
    path = os.path.join(SAVE_DIR, "training_history.png")
    plt.savefig(path, dpi=120, bbox_inches="tight")
    plt.show()
    print(f"Saved: {path}")

