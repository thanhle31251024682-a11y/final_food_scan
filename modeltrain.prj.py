    splits = {"val": imgs[:n_val], "train": imgs[n_val:]}

    for split, files in splits.items():
        dest = os.path.join(SPLIT_DIR, split, class_name)
        os.makedirs(dest, exist_ok=True)
        for f in files:
            src_file_path = os.path.join(src, f)
            try:
                shutil.copy2(src_file_path, os.path.join(dest, f))
            except FileNotFoundError:
                print(f"⚠️  File not found and skipped: {src_file_path}")

    print(f"  {class_name}: {len(splits['train'])} train | {n_val} val")

