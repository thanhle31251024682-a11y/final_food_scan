# CELL 4+5 GỘP — Load model + tất cả hàm cần thiết
import json, base64
import numpy as np
import tensorflow as tf
from PIL import Image
from io import BytesIO

# Load model
print('Đang load model...')
model = tf.keras.models.load_model(MODEL_PATH)
print('✅ Load model thành công!')

# Load class indices
with open(CLASS_INDICES_PATH, 'r', encoding='utf-8') as f:
    class_indices = json.load(f)
idx_to_class = {v: k for k, v in class_indices.items()}

# Build bảng giá
import re
PRICE_BY_STT = {
    1: 10000, 2: 25000, 3: 30000, 4: 30000, 5: 25000,
    6: 10000, 7: 30000, 8: 7000,  9: 10000, 10: 25000,
}
def extract_stt(class_name):
    m = re.match(r'^(\d+)\.', class_name.strip())
    return int(m.group(1)) if m else None
def clean_name(class_name):
    return re.sub(r'^\d+\.\s*', '', class_name.strip())

MENU_PRICES, DISPLAY_NAMES = {}, {}
for class_name in class_indices.keys():
    stt = extract_stt(class_name)
    MENU_PRICES[class_name]   = PRICE_BY_STT.get(stt, 0) if stt else 0
    DISPLAY_NAMES[class_name] = clean_name(class_name) if stt else class_name

# Hàm predict 1 ngăn
def predict_compartment(crop: Image.Image, size=(224, 224)):
    arr = np.array(crop.resize(size, Image.LANCZOS)) / 255.0
    arr = np.expand_dims(arr, 0)
    preds = model.predict(arr, verbose=0)
    idx  = int(np.argmax(preds[0]))
    conf = float(np.max(preds[0]))
    return idx_to_class.get(idx, 'Không xác định'), conf

# Hàm nhận crops từ JS
def analyze_tray_crops(crops_data):
    results, total = [], 0
    for item in crops_data:
        img_bytes = base64.b64decode(item['base64'])
        crop = Image.open(BytesIO(img_bytes)).convert('RGB')
        cls, conf = predict_compartment(crop)
        price  = MENU_PRICES.get(cls, 0)
        display = DISPLAY_NAMES.get(cls, cls)
        total += price
        results.append({
            'compartment': item['name'],
            'food_name':   display,
            'raw_class':   cls,
            'confidence':  round(conf * 100, 1),
            'price':       price,
            'crop_image':  item['base64'],
        })
    return results, total

print('✅ Tất cả hàm sẵn sàng!')
print(f'✅ {len(idx_to_class)} món: {list(DISPLAY_NAMES.values())}')

