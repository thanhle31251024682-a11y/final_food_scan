@app.route('/analyze', methods=['POST'])
def analyze():
    try:
        data = request.get_json()
        print('📦 Keys nhận được:', list(data.keys()) if data else 'None')
        print('📦 crops có không:', 'crops' in data if data else False)

        if not data:
            return jsonify({'error': 'Không có data'}), 400

        if 'crops' in data:
            results, total = analyze_tray_crops(data['crops'])
        elif 'image' in data:
            img_data = data['image']
            if ',' in img_data:
                img_data = img_data.split(',')[1]
            image_bytes = base64.b64decode(img_data)
            results, total = analyze_tray(image_bytes)
        else:
            return jsonify({'error': 'Thiếu crops hoặc image'}), 400

        return jsonify({'success': True, 'items': results, 'total': total})

    except Exception as e:
        import traceback; traceback.print_exc()
        return jsonify({'error': str(e)}), 500

